# This program shows how different arithmatic operations are performed in python

x = 50
y = 12

# Addition operator
print(x + y)
# Subtraction
print(x - y)
# Multiplication
print(x * y)
# Division
print(x / y) # Floating point division, returns a floating point number
print(x // y) # Floor/ integer division,only returns an integer number
# Modulation
print(x % y) # Returns the reminder of the division
# Exponent
print(x ** y) # Returns the first numbers to the power of the second number
# incrementation
y = y + 1
print(y)
# argumented assignment
y += 8
print(y)

